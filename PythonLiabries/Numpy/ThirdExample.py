import numpy as np
#The Use of The Empty() in Numpy Array
arr = np.empty((3,2),dtype=complex)
print("The Numpy Array is ")
print(arr)

# The Use of The Zeros() in Numpy Array

arr = np.zeros((3,3),dtype=int)
print("The Zeroes of The Numpy Array is ")
print(arr)

# The use of The Ones() in Numpy Array

arr = np.ones((2,3),dtype=int)
print("The ones of The Numpy Array is ")
print(arr)





